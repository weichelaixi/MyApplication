package com.weiche.module_girls.grils.bean;

import android.os.Parcel;
import android.os.Parcelable;

/**
 * Created by ${chewei} on 2018/6/29.
 * params:2018/6/29
 */

public class Girls implements Parcelable {
    private String _id;
    private String createdAt;
    private String desc;
    private String publishedAt;
    private String source;
    private String type;
    private String url;
    private boolean used;
    private String who;

    public String get_id() {
        return _id;
    }

    public void set_id(String _id) {
        this._id = _id;
    }

    public String getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(String createdAt) {
        this.createdAt = createdAt;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public String getPublishedAt() {
        return publishedAt;
    }

    public void setPublishedAt(String publishedAt) {
        this.publishedAt = publishedAt;
    }

    public String getSource() {
        return source;
    }

    public void setSource(String source) {
        this.source = source;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public boolean isUsed() {
        return used;
    }

    public void setUsed(boolean used) {
        this.used = used;
    }

    public String getWho() {
        return who;
    }

    public void setWho(String who) {
        this.who = who;
    }

    protected Girls(Parcel in) {
        this._id = in.readString();
        this.createdAt = in.readString();
        this.desc = in.readString();
        this.publishedAt = in.readString();
        this.source = in.readString();
        this.type = in.readString();
        this.url = in.readString();
        this.used = in.readByte() != 0;
        this.who = in.readString();
    }

    public static final Creator<Girls> CREATOR = new Creator<Girls>() {
        @Override
        public Girls createFromParcel(Parcel in) {
            return new Girls(in);
        }

        @Override
        public Girls[] newArray(int size) {
            return new Girls[size];
        }
    };

    @Override
    public int describeContents() {
        return 0;
    }

    @Override
    public void writeToParcel(Parcel parcel, int i) {
        parcel.writeString(this._id);
        parcel.writeString(this.createdAt);
        parcel.writeString(this.desc);
        parcel.writeString(this.source);
        parcel.writeString(this.publishedAt);
        parcel.writeString(this.type);
        parcel.writeString(this.url);
        parcel.writeString(this.who);
        parcel.writeByte(this.used ? (byte) 1:(byte)0);
    }
}
